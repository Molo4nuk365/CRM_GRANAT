using CRM_Jewelry_workshop.DTO;       
// Подключаем DTO-классы (CreateOrderDto, OrderDto),          
// используемые для приёма/передачи данных через сервис
using CRM_Jewelry_workshop.Models;    
// Подключаем модели предметной области (Order и др.)

namespace CRM_Jewelry_workshop.Services
{
   
    // Интерфейс сервиса для управления заказами
    // Все методы асинхронны и возвращают Task, что позволяет не блокировать поток выполнения
    public interface IOrderService
    {
      
        //Создаёт новый заказ от имени клиента.
        //clientUserId">Идентификатор пользователя-клиента, оформляющего заказ.
        // DTO с данными для создания заказа (материалы, количество, пожелания и т.п.)
       //Созданный объект Order после сохранения в базе.
        Task<Order> CreateOrderAsync(int clientUserId, CreateOrderDto dto);

       
        // Получает список заказов, доступных пользователю в зависимости от его роли
        // Клиент видит свои заказы, менеджер — все заказы, ювелир — назначенные ему
        // Id пользователя, запрашивающего список
        // role >Роль пользователя ("Client", "Manager", "Jeweler") — влияет на фильтрацию.
        // Список DTO заказов для отображения в интерфейсе
        Task<List<OrderDto>> GetOrdersForUserAsync(int userId, string role);

       
        // Менеджер принимает заказ в работу (подтверждает, что он корректен и может быть выполнен).
        //orderId">Id заказа.
        //managerId">Id менеджера, выполняющего приёмку
        //Обновлённый заказ или null, если заказ не найден или действие невозможно.
        Task<Order?> AcceptOrderAsync(int orderId, int managerId);

      
        //Ювелир самостоятельно берёт заказ в работу (назначает себя исполнителем).
        //orderId">Id заказа.
        //jewelerId >Id ювелира, который берёт заказ.
        //Обновлённый заказ или null при ошибке/отсутствии заказа.
        Task<Order?> TakeOrderAsync(int orderId, int jewelerId);

        
        // Менеджер назначает конкретного ювелира исполнителем заказа.
        // orderId">Id заказа.
        //"jewelerId">Id назначаемого ювелира.
        //Обновлённый заказ или null, если операция не выполнена.
        Task<Order?> AssignJewelerAsync(int orderId, int jewelerId);

        
        // Ювелир отмечает, что заказ выполнен (изделие готово)
        // orderId">Id заказа.
        // Заказ в статусе "Готов" или null в случае ошибки.
        Task<Order?> ReadyOrderAsync(int orderId);


        // Менеджер закрывает заказ как завершённый (после выдачи клиенту, например).
        //orderId >Id заказа.
       //Заказ в финальном статусе или null при невозможности операции.
        Task<Order?> CompleteOrderAsync(int orderId);

        // Отменяет заказ (клиентом или менеджером) — переводит в статус "Отменён".
        //orderId">Id заказа.
        //Отменённый заказ или null, если отмена не разрешена.
        Task<Order?> CancelOrderAsync(int orderId);

        
        //Устанавливает или изменяет крайний срок выполнения заказа.
        // Доступно менеджеру и, возможно, клиенту с ограничениями (роль проверяется внутри).
     
        //orderId >Id заказа.
        // deadline > Новая дата и время дедлайна.
        //currentUserRole >Роль пользователя, выполняющего операцию.
        // Обновлённый заказ или null при ошибке.
        Task<Order?> SetDeadlineAsync(int orderId, DateTime deadline, string currentUserRole);

       
        // Получает заказ по его уникальному идентификатору вместе со всеми связанными данными.
        // orderId >Id заказа.
        // Найденный заказ или null, если не существует
        Task<Order?> GetOrderByIdAsync(int orderId);
    }
}