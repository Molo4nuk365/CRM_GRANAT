namespace CRM_Jewelry_workshop.DTO
{
    // Класс OrderDto — это DTO (Data Transfer Object), объект передачи данных о заказе.
    // Он используется для того, чтобы передавать клиенту (браузеру/мобильному приложению)
    // только необходимые поля заказа, уже «разложенные» в удобном для отображения виде,
    // без полных навигационных сущностей (Order, Client, Manager, Jeweler).
    public class OrderDto
    {
        // Идентификатор заказа — всегда присутствует
        public int OrderId { get; set; }

        // Полное имя клиента. Может быть null, если клиент не задан или не загружен.
        // Тип string? допускает значение null.
        public string? ClientName { get; set; }

        // Имя менеджера, работающего с заказом. Может быть null, если менеджер ещё не назначен.
        public string? ManagerName { get; set; }

        // Имя ювелира-исполнителя. Может быть null, если ювелир не назначен.
        public string? JewelerName { get; set; }

        // Идентификатор ювелира. Nullable (int?), потому что ювелира может и не быть.
        // Может использоваться на фронтенде, чтобы, например, подсветить свои заказы у ювелира
        // или разрешить ему брать заказ, только если JewelerId == null.
        public int? JewelerId { get; set; }

        // Название текущего статуса заказа (например, "Новый", "Принят", "В работе").
        // Может быть null, если по какой-то причине статус отсутствует.
        public string? Status { get; set; }

        // Итоговая стоимость заказа. Здесь используется decimal для точности вычислений,
        // как и в моделях. Не nullable, потому что стоимость 0 — допустимое начальное значение.
        public decimal TotalCost { get; set; }

        // Дата и время создания заказа. Не nullable, всегда известна.
        public DateTime CreateDate { get; set; }

        // Крайний срок выполнения заказа (дедлайн). Может быть не установлен,
        // поэтому допускает null — DateTime?.
        public DateTime? Deadline { get; set; }
    }
}