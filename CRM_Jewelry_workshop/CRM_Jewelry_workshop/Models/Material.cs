// Подключаем пространство имён System.ComponentModel.DataAnnotations,
// которое содержит атрибуты для декларативной валидации и аннотаций данных.
// Здесь используется атрибут [Key] для указания первичного ключа сущности.
using System.ComponentModel.DataAnnotations;

// Пространство имён, в котором определены все модели данных приложения.
// Это помогает структурировать проект и избегать конфликтов имён.
namespace CRM_Jewelry_workshop.Models;

// Класс Material представляет справочник материалов, используемых в ювелирном производстве.
// Он описывает как расходные материалы (серебро, золото), так и вставки (гранат, бриллиант).
// Является сущностью Entity Framework Core, которая отображается на таблицу в базе данных.
public class Material
{
    // [Key] — атрибут, указывающий Entity Framework, что свойство MaterialId 
    // является первичным ключом таблицы. 
    [Key]
    public int MaterialId { get; set; }

    // Название материала (например, "Серебро 925 пробы", "Гранат натуральный").
    // Инициализируется пустой строкой, чтобы избежать предупреждений о возможном null 
    public string Name { get; set; } = string.Empty;

    // Текстовое описание материала: характеристики, происхождение, особенности.
    // Значение по умолчанию — пустая строка, чтобы гарантировать, что свойство не null.
    public string Description { get; set; } = string.Empty;

    // Единица измерения материала (например, "г" — граммы, "кар" — караты, "шт" — штуки).
    // Это строка, потому что не все единицы поддаются формальному перечислению.
    // Позволяет системе корректно интерпретировать количество при расчётах.
    public string Unit { get; set; } = string.Empty;      
    // Примеры значений: "г" (граммы), "кар" (караты), "шт" (штуки).

    // Тип decimal выбран для финансовой точности: он минимизирует ошибки округления,
    // неизбежные при использовании float или double.
    public decimal PricePerUnit { get; set; }

    // Текущее количество материала на складе. Может быть дробным числом, 
    // так как многие материалы измеряются в долях (например, 15.7 г серебра).
    // Используется decimal для точного учёта.
    public decimal QuantityInStock { get; set; }

    // Навигационное свойство для связи "один-ко-многим" с сущностью Position (позиция заказа).
    // Один материал может входить во множество позиций заказов.
    // Тип ICollection<Position>? — коллекция позиций, может быть null, если связанные 
    // сущности не загружены (при отложенной загрузке). Включает знак ? из-за nullable reference types.
    // Позволяет EF Core автоматически создавать внешний ключ MaterialId в таблице Position.
    public ICollection<Position>? Positions { get; set; }
}