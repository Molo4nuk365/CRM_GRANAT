using System.ComponentModel.DataAnnotations;
// Пространство имён содержит атрибуты аннотации данных (data annotations).
// Основные атрибуты: [Key], [Required], [MaxLength], [Range] и т.д.
// Здесь используется [Key] для явного указания первичного ключа сущности.

using System.ComponentModel.DataAnnotations.Schema;
// Содержит атрибуты, описывающие отображение на схему базы данных.
// [ForeignKey] — задаёт свойство-внешний ключ для навигационного свойства.
// [Table] — позволяет переопределить имя таблицы (здесь не используется).

// Пространство имён, где хранятся все модели данных приложения CRM ювелирной мастерской.
namespace CRM_Jewelry_workshop.Models;

// Класс Position представляет строку в заказе — связку «какой материал и в каком количестве».
// Это реализация связи «многие-ко-многим» между Order и Material с дополнительными полями
// (цена на момент заказа и количество), то есть таблица-связка с полезной нагрузкой.
public class Position
{
    // [Key] — атрибут, указывающий, что PositionId — это первичный ключ (Primary Key).
    // EF Core по умолчанию мог бы распознать это свойство как ключ по имени (Id или PositionId),
    // но атрибут делает намерение явным и защищает от ошибок при переименовании.
    [Key]
    public int PositionId { get; set; }

    // Внешний ключ к таблице Orders.
    // Тип int не допускает null, поэтому связь с заказом является обязательной —
    // каждая позиция обязательно принадлежит какому-либо заказу.
    public int OrderId { get; set; }

    // [ForeignKey(nameof(OrderId))] — сообщает EF Core, что свойство OrderId является внешним ключом
    // для навигационного свойства Order. nameof() используется для типобезопасной ссылки на имя свойства.
    [ForeignKey(nameof(OrderId))]

    // Навигационное свойство к сущности Order. Знак "?" (Nullable Reference Type) показывает,
    // что компилятор допускает null для этого свойства, например, если заказ не загружен явно.
    // При правильной загрузке данных (Include/ThenInclude) оно будет заполнено объектом Order.
    public Order? Order { get; set; }

    // Внешний ключ к таблице Materials.
    // Обязательное поле — каждая позиция ссылается на конкретный материал.
    public int MaterialId { get; set; }
    // Указывает, что MaterialId — внешний ключ для навигационного свойства Material.

    [ForeignKey(nameof(MaterialId))]
    // Навигационное свойство к сущности Material. Может быть null, если не загружено.
    public Material? Material { get; set; }

    // Цена за единицу материала именно на момент оформления заказа.
    // Сохраняется в позиции, чтобы зафиксировать историческую цену, даже если цена материала позже изменится.
    // Тип decimal выбран для высокой точности при денежных расчётах (до 28 знаков после запятой).
    public decimal Price { get; set; }

    // Количество материала в заказе. Может быть дробным (например, 1.5 грамма золота, 0.03 карата камня).
    // Тип decimal позволяет корректно хранить такие величины.
    public decimal Quantity { get; set; }
}