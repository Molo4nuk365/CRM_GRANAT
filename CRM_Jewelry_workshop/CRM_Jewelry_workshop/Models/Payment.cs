// Подключаем пространства имён для атрибутов валидации и работы с внешними ключами
using System.ComponentModel.DataAnnotations;
// Содержит атрибуты аннотации данных, например [Key] для указания первичного ключа.
using System.ComponentModel.DataAnnotations.Schema;
// Содержит атрибуты для схемы базы данных, в частности [ForeignKey] для настройки связей.

// Пространство имён, где определены все модели данных приложения CRM ювелирной мастерской.
namespace CRM_Jewelry_workshop.Models;

// Класс Payment представляет платёж, совершённый по конкретному заказу.
// Может использоваться для учёта частичной или полной оплаты, а также для истории транзакций.
public class Payment
{
    // [Key] — PaymentId является первичным ключом.
    [Key]
    public int PaymentId { get; set; }

    // Внешний ключ, связывающий платёж с заказом.
    // Не nullable (тип int), следовательно, каждый платёж обязательно привязан к заказу.
    public int OrderId { get; set; }

    // [ForeignKey(nameof(OrderId))] — указывает EF Core, что свойство OrderId
    // является внешним ключом для навигационного свойства Order.
    // nameof() используется для избежания ошибок при переименовании свойств.
    [ForeignKey(nameof(OrderId))]
    // Навигационное свойство для доступа к объекту заказа, к которому относится платёж.
    // Знак ? (nullable reference type) означает, что свойство может быть null,
    // если связанный заказ не загружен (например, при ленивой загрузке).
    public Order? Order { get; set; }

    // Сумма платежа. Тип decimal обеспечивает высокую точность для финансовых операций,
    // избегая ошибок округления, свойственных float или double.
    public decimal Amount { get; set; }

    // Дата и время совершения платежа. Фиксирует момент, когда платёж был произведён.
    public DateTime Date { get; set; }

    // Внешний ключ на таблицу статусов платежа (StatusPayment).
    // Определяет, находится ли платёж в статусе "Оплачен", "Ожидает", "Отменён" и т.д.
    public int StatusPaymentId { get; set; }

    // Связываем StatusPaymentId с навигационным свойством StatusPayment.
    [ForeignKey(nameof(StatusPaymentId))]
    // Навигационное свойство для доступа к объекту статуса платежа.
    // Может быть null, если статус не загружен.
    public StatusPayment? StatusPayment { get; set; }
}